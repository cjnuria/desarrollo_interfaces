    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // a√±adir especialidad a la tabla
       
        String nombre = jtnombreCuidador2.getText();
        String apellidos = jtapelillodosCuidador.getText();
        String cargo = jComboBox1.getSelectedItem().toString();

        // controlar campos
        if (nombre.equals("") || apellidos.equals("") || jtcuidadosTelefono.getText().equals("") || cargo.equals("") || jtcuidadosSalario.getText().equals("") || jcomboespecialidad.getSelectedItem().equals(null)) {
            JOptionPane.showMessageDialog(null, "Los campos no pueden estar vacios.");

        } else {
           int id = Integer.parseInt(jtid_persona.getText());
            int telefono = Integer.parseInt(jtcuidadosTelefono.getText());
            float salario = Float.parseFloat(jtcuidadosSalario.getText());

            boolean comprobar = DBManager_login.actualizar_persona(id,nombre, apellidos, telefono, cargo, salario);
            if (comprobar) {
                JOptionPane.showMessageDialog(null, "A√±adido correctamente");

            } else {
                JOptionPane.showMessageDialog(null, "Ha ocurrido un error");
            }
        }
         dispose();
    }//GEN-LAST:event_jButton3ActionPerformed

