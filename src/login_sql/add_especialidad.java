    private void botonGuardarEspecialidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarEspecialidadActionPerformed
        if (!metodos.Utilidades_Control.contieneSoloLetras(jTextEspecialidad.getText())) {
            JOptionPane.showMessageDialog(null, "Solo se pueden introducir letras");

        } else {
            String especialidad = jTextEspecialidad.getText();

            boolean comprobar = DBManager_login.add_especialidad(especialidad);
            if (comprobar) {
                JOptionPane.showMessageDialog(null, "Se ha aÃ±adido correctamente");
                dispose();

            } else {
                JOptionPane.showMessageDialog(null, "No se ha aÃ±adido correctamente");

            }

        }


    }//GEN-LAST:event_botonGuardarEspecialidadActionPerformed

