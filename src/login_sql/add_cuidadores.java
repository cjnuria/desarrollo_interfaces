    public void rellenar_tabla_cuidadores() {
        String sql = "Select * from personas";
        try {
            ResultSet resultado = DBManager_login.obtener_tablas(DEFAULT_CURSOR, DISPOSE_ON_CLOSE, sql);
            while (resultado.next()) {
                int id_persona = resultado.getInt("id_persona");
                String nombre = resultado.getString("nombre");
                String apellidos = resultado.getString("apellidos");
                String telefono = resultado.getString("telefono");
                String cargo = resultado.getString("cargo");
                String salario = resultado.getString("salario");

                DefaultTableModel model = (DefaultTableModel) jTablelistar_cuidadores.getModel();
                Object[] row = {id_persona, nombre, apellidos, telefono, cargo, salario};
                model.addRow(row);
            }
            resultado.close();

        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }

    //___________________________________________________________________________________________________
    // Vaciar tabla animales para no duplicar datos 
    public void vaciar_tablas() {

        //vaciamos la tabla recorriendola con un bucle for
        DefaultTableModel tb = (DefaultTableModel) jTablelistar_cuidadores.getModel();
        int a = jTablelistar_cuidadores.getRowCount() - 1; //contamos las filas
        //bucle para borrar todas las filas
        for (int i = a; i >= 0; i--) {
            tb.removeRow(tb.getRowCount() - 1);
        }

    }

