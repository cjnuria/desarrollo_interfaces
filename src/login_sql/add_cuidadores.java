    private void jButtonModificar_cuidadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificar_cuidadorActionPerformed
        // editar cuidadores
        int fila = jTablelistar_cuidadores.getSelectedRow();// coge la fila que has selecionado 
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "No se ha selecionado ninguna fila");

        } else {
            int id_persona = (int) jTablelistar_cuidadores.getValueAt(fila, 0);// este coje la columna 0 que es la del id
            String nombre = (String) jTablelistar_cuidadores.getValueAt(fila, 1);
            String apellidos = (String) jTablelistar_cuidadores.getValueAt(fila, 2);
            String telefono = (String) jTablelistar_cuidadores.getValueAt(fila, 3);
            String cargo = (String) jTablelistar_cuidadores.getValueAt(fila, 4);
            String salario = (String) jTablelistar_cuidadores.getValueAt(fila, 5);
            editores.editar_cuidador open = new editores.editar_cuidador(id_persona, nombre, apellidos, telefono, cargo, salario);
            open.setVisible(true);

// actualizar tabla
            vaciar_tablas();
            rellenar_tabla_cuidadores();
        }
    }//GEN-LAST:event_jButtonModificar_cuidadorActionPerformed

