    private void botonAceptarCuidadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarCuidadosActionPerformed
       
        if (!metodos.Utilidades_Control.contieneSoloLetras(jTextFieldaddEspecie.getText())) {
            JOptionPane.showMessageDialog(null, "Solo se pueden introducir letras");
        } else {
            String nombre_especie = jTextFieldaddEspecie.getText();

            boolean comprobar = DBManager_login.add_especie(nombre_especie);
            if (comprobar) {
                JOptionPane.showMessageDialog(null, "Se ha a単adido correctamente");
                dispose();

            } else {
                JOptionPane.showMessageDialog(null, "No se ha a単adido correctamente");

            }

        }

    }//GEN-LAST:event_botonAceptarCuidadosActionPerformed

